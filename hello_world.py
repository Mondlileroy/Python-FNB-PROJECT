#Advanced concepts - strings

message2 = "Hello"
print(message2[0])
print(message2[1])

print(message2[-1]) # Last character

print(len(message2)) # Length of the string
print(message2.strip()) # Remove whitespace
print(message2.lower()) # Convert to lowercase
print(message2.upper()) # Convert to uppercase
print(message2.split(', ')) # Split the string by a comma and space

