fruits = ["apple", "banana", "cherry"]

fruits.append("kiwi") # Adding a new element to the end of the list
print(fruits) 

fruits.insert(1, "orange") # Inserting an element at a specific position
print(fruits)

fruits.remove("banana") # Removing an element by value
print(fruits)

fruits.sort() # Sorting the list in ascending order
print(fruits)

fruits.reverse() # Reversing the order of the list
print(fruits)